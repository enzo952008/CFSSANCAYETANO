function cantidadDeDivisores(numero:number):number{
    let divisores=0;
    let numeroDivisor=0
    for(let i=1; i<=numeroDivisor; i++){
        if(numeroDivisor%i===0){
            divisores++
        }
    }
    return divisores;
}
